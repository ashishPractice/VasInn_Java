package day8;

public class Employee {
    private String empName;
    private int empId;

    public String getEmpName() {
        return empName;
    }

    public void setEmpName(String name) {
        empName = name;
    }

    public int getEmpId() {
        return empId;
    }

    public void setEmpId(int id) {
        empId = id;
    }


}
